int		main(int argc, char *argv[])
{
	int i;
	int j;

	i = 1;
	j = 0;
	while (i < argc)
	{
		while (argv[i][j] != '\0')
		{
			ft_putchar(argv[i][j++]);
		}
		ft_putchar('\n');
		j = 0;
		i++;
	}
}
