int		ft_count_if(char **tab, int (*f)(char *))
{
	int		r;
	int		i;

	i = 0;
	r = 0;
	while (tab[i] != '\0')
	{
		if (f(tab[i]) == 1)
			r++;
		i++;
	}
	return (r);
}
