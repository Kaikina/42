int		ft_sqrt(int nb)
{
	int		sqr;
	int		i;

	i = 1;
	sqr = 1;
	if (nb == 0 | nb < 0)
		return (0);
	if (nb > 0)
	{
		while (i <= nb)
		{
			sqr = 0.5 * (sqr + nb / sqr);
			i++;
		}
	}
	if (nb % sqr != 0)
		return (0);
	return (sqr);
}
